// ⚠️ 표준 템플릿이며 법적 검토를 대체하지 않음. 창업자는 필요 시 변호사 검토 권장.

export type BizInfo = {
  name: string;
  company?: string | null;   // 상호
  owner?: string | null;     // 대표자
  number?: string | null;    // 사업자등록번호
  mailorder?: string | null; // 통신판매업 신고번호
  address?: string | null;
  phone?: string | null;
  email?: string | null;
};

const won = (v?: string | null) => (v && v.trim() ? v.trim() : "―");

